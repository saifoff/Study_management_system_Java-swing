    private void table_update(){
        int c;
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/coursemanagement","root",""); //set connection with database
            insert = con1.prepareStatement("select * from examroutine");
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
            Df.setRowCount(0);
            while(rs.next())
            {
                Vector v2 = new Vector();
                for(int a =1 ;a<=c; a++){
                    v2.add(rs.getInt("exam_no"));
                    v2.add(rs.getDate("Exam_Date"));
                    v2.add(rs.getString("Exam_Time"));
                    v2.add(rs.getString("Exam_Subject"));
                    
                }
                Df.addRow(v2); 
            }
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(CourseTask.class.getName()).log(Level.SEVERE, null, ex);
           
        }
    
    }

