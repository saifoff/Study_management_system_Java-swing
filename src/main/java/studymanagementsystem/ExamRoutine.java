            String examname = ExamSubjectjTextField.getText();  //get input from text field2
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/coursemanagement", "root", ""); //set connection with database
            insert = con1.prepareStatement("update examroutine set Exam_Date=?,Exam_Time=?,Exam_Subject=? where exam_no=?");
            insert.setDate(1, examdate);
            insert.setString(2, examtime);
            insert.setString(3, examname);
